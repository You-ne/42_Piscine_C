#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	i;
	int				size;

	if (s && f)
	{
		i = 0;
		size = ft_strlen((char *)s);
		if (!(str = (char*)malloc((size + 1) * sizeof(char))))
			return (NULL);
		while (s[i])
		{
			str[i] = f(i, s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
