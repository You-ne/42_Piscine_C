#include "libft.h"

int	ft_atoi(const char *str)
{
	int					i;
	unsigned int		res;
	int					sign;

	sign = 1;
	i = 0;
	res = 0;
	while ((*str > 8 && *str < 14) || *str == ' ')
		str++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
		res = res * 10 + (str[i++] - '0');
	return ((int)res * sign);
}
