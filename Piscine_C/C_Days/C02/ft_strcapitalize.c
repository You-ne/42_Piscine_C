char	*ft_strcapitalize(char *str)
{
	int i;
	
	i = 1;
	while (str[i])
	{
		if ((96 < str[i] && str[i] < 123) && (str[i - 1] < 48 || ((57 < 
			str[i - 1] && str[i - 1] < 65) || (90 < str[i -1]  && 
			str[i - 1] < 97)) || 122 < str[i - 1]))
			str[i] -= 32;
		if ((64 < str[i] && str[i] < 91) && ((64 < str[i - 1] && str[i - 1] < 91) || (96 < str[i - 1] && str[i -1 ] < 123)))
			str[i] += 32;
		i++;
	}
	return (str);
}
